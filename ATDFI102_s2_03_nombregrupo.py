#área del trapecio
base_superior=float(input('Ingresa la longitud en cms de la base superior: '))
base_inferior=float(input('Ingresa la longitud en cms de la base Inferior: '))
altura=float(input('Ingresa la altura en: '))
area=(altura*((base_superior+base_inferior)/2))
print('El área del trapecio es: %.2f cms2' %(area))